async function main() {
  console.log('🔄 Seeding klasifikasi arsip data...');
  console.log(`   Total records: ${KLASIFIKASI_DATA.length}`);

  let created = 0;
  let skipped = 0;

  for (const [kode, nama] of KLASIFIKASI_DATA) {
    try {
      await prisma.klasifikasi_arsip.upsert({
        where: { kode },
        update: { nama, level: getLevel(kode), parent_kode: getParentKode(kode) },
        create: {
          kode,
          nama,
          level: getLevel(kode),
          parent_kode: getParentKode(kode),
          is_active: true,
        },
      });
      created++;
    } catch (err) {
      console.error(`   ❌ Failed: ${kode} - ${err.message}`);
      skipped++;
    }
  }

  console.log(`✅ Seeding complete: ${created} upserted, ${skipped} failed`);
}

main()
  .catch((e) => {
    console.error('❌ Seeding failed:', e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
