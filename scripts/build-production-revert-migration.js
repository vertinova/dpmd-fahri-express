const fs = require('fs');
const path = require('path');

const DEFAULT_REPORT = 'logs/no-log-migration-candidates-2026-05-09_10-41-32.json';
const DEFAULT_OUTPUT = 'migrations/20260509_revert_generated_kelembagaan_9_kecamatan.sql';
const MIGRATION_START = '2026-04-18 18:50:00';
const MIGRATION_END = '2026-04-18 19:20:00';
const BACKUP_PREFIX = 'rollback_20260509_generated';
const CHUNK_SIZE = 400;
const DELETE_ORDER = ['pengurus', 'rts', 'rws'];

const args = parseArgs(process.argv.slice(2));
const reportPath = path.resolve(process.cwd(), args.report || DEFAULT_REPORT);
const outputPath = path.resolve(process.cwd(), args.output || DEFAULT_OUTPUT);

main();

function main() {
  if (!fs.existsSync(reportPath)) {
    throw new Error(`Report not found: ${reportPath}`);
  }

  const report = JSON.parse(fs.readFileSync(reportPath, 'utf8'));
  const safeRows = report.details
    .filter((detail) => detail.status === 'safe_candidate')
    .filter((detail) => DELETE_ORDER.includes(detail.table));

  const byTable = groupBy(safeRows, (detail) => detail.table);
  const sql = buildSql(report, byTable);

  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, sql);

  console.log(`Wrote migration: ${outputPath}`);
  for (const table of DELETE_ORDER) {
    console.log(`- ${table}: ${(byTable.get(table) || []).length}`);
  }
}

function buildSql(report, byTable) {
  const lines = [
    '-- Revert generated RT/RW kelembagaan migration data for selected kecamatan.',
    '-- Generated by scripts/build-production-revert-migration.js',
    `-- Source report: ${path.relative(process.cwd(), reportPath).replace(/\\/g, '/')}`,
    `-- Candidate window: ${MIGRATION_START} to ${MIGRATION_END}`,
    '--',
    '-- Safety rules:',
    '-- 1. Only hardcoded candidate IDs from the reviewed scan are considered.',
    '-- 2. Every delete is guarded by created_at window and absence of kelembagaan_activity_logs.',
    '-- 3. Parent RT/RW records are deleted only when no child pengurus/RT remains.',
    '-- 4. Rows are backed up into rollback_20260509_generated_* tables before deletion.',
    '',
    'CREATE TABLE IF NOT EXISTS `rollback_20260509_generated_pengurus` LIKE `pengurus`;',
    'CREATE TABLE IF NOT EXISTS `rollback_20260509_generated_rts` LIKE `rts`;',
    'CREATE TABLE IF NOT EXISTS `rollback_20260509_generated_rws` LIKE `rws`;',
    '',
    'CREATE TABLE IF NOT EXISTS `rollback_20260509_generated_summary` (',
    '  `table_name` VARCHAR(64) NOT NULL PRIMARY KEY,',
    '  `candidate_ids` INT NOT NULL DEFAULT 0,',
    '  `safe_rows` INT NOT NULL DEFAULT 0,',
    '  `backup_rows` INT NOT NULL DEFAULT 0,',
    '  `deleted_rows` INT NOT NULL DEFAULT 0,',
    '  `ran_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP',
    ') ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;',
    '',
    'START TRANSACTION;',
    ''
  ];

  appendTableSection(lines, 'pengurus', byTable.get('pengurus') || [], {
    fromTable: 'pengurus',
    alias: 'p',
    safetyWhere: [
      '`p`.`created_at` BETWEEN @migration_window_start AND @migration_window_end',
      'NOT EXISTS (SELECT 1 FROM `kelembagaan_activity_logs` `l` WHERE `l`.`entity_id` = `p`.`id` OR `l`.`kelembagaan_id` = `p`.`id`)'
    ],
    deleteSql: 'DELETE `p` FROM `pengurus` `p` INNER JOIN `tmp_safe_pengurus_20260509` `s` ON `s`.`id` = `p`.`id`;'
  });

  appendTableSection(lines, 'rts', byTable.get('rts') || [], {
    fromTable: 'rts',
    alias: 'rt',
    safetyWhere: [
      '`rt`.`created_at` BETWEEN @migration_window_start AND @migration_window_end',
      'NOT EXISTS (SELECT 1 FROM `kelembagaan_activity_logs` `l` WHERE `l`.`entity_id` = `rt`.`id` OR `l`.`kelembagaan_id` = `rt`.`id`)',
      'NOT EXISTS (SELECT 1 FROM `pengurus` `p` WHERE `p`.`pengurusable_type` IN (\'rts\', \'rt\') AND `p`.`pengurusable_id` = `rt`.`id`)'
    ],
    deleteSql: 'DELETE `rt` FROM `rts` `rt` INNER JOIN `tmp_safe_rts_20260509` `s` ON `s`.`id` = `rt`.`id`;'
  });

  appendTableSection(lines, 'rws', byTable.get('rws') || [], {
    fromTable: 'rws',
    alias: 'rw',
    safetyWhere: [
      '`rw`.`created_at` BETWEEN @migration_window_start AND @migration_window_end',
      'NOT EXISTS (SELECT 1 FROM `kelembagaan_activity_logs` `l` WHERE `l`.`entity_id` = `rw`.`id` OR `l`.`kelembagaan_id` = `rw`.`id`)',
      'NOT EXISTS (SELECT 1 FROM `pengurus` `p` WHERE `p`.`pengurusable_type` IN (\'rws\', \'rw\') AND `p`.`pengurusable_id` = `rw`.`id`)',
      'NOT EXISTS (SELECT 1 FROM `rts` `rt` WHERE `rt`.`rw_id` = `rw`.`id`)'
    ],
    deleteSql: 'DELETE `rw` FROM `rws` `rw` INNER JOIN `tmp_safe_rws_20260509` `s` ON `s`.`id` = `rw`.`id`;'
  });

  lines.push(
    'COMMIT;',
    '',
    'SELECT * FROM `rollback_20260509_generated_summary` ORDER BY FIELD(`table_name`, \'pengurus\', \'rts\', \'rws\');',
    ''
  );

  return lines.join('\n');
}

function appendTableSection(lines, table, rows, options) {
  const tmpCandidate = `tmp_candidate_${table}_20260509`;
  const tmpSafe = `tmp_safe_${table}_20260509`;
  const backupTable = `${BACKUP_PREFIX}_${table}`;
  const alias = options.alias;

  lines.push(
    `-- ${table}: ${rows.length} reviewed candidate IDs`,
    `SET @migration_window_start = ${sqlString(MIGRATION_START)};`,
    `SET @migration_window_end = ${sqlString(MIGRATION_END)};`,
    `DROP TEMPORARY TABLE IF EXISTS \`${tmpCandidate}\`;`,
    `DROP TEMPORARY TABLE IF EXISTS \`${tmpSafe}\`;`,
    `CREATE TEMPORARY TABLE \`${tmpCandidate}\` (\`id\` CHAR(36) NOT NULL PRIMARY KEY) ENGINE=MEMORY;`
  );

  for (const chunk of chunks(rows.map((row) => row.id), CHUNK_SIZE)) {
    lines.push(
      `INSERT IGNORE INTO \`${tmpCandidate}\` (\`id\`) VALUES`,
      `${chunk.map((id) => `  (${sqlString(id)})`).join(',\n')};`
    );
  }

  lines.push(
    `CREATE TEMPORARY TABLE \`${tmpSafe}\` (\`id\` CHAR(36) NOT NULL PRIMARY KEY) ENGINE=MEMORY;`,
    `INSERT IGNORE INTO \`${tmpSafe}\` (\`id\`)`,
    `SELECT \`${alias}\`.\`id\``,
    `FROM \`${options.fromTable}\` \`${alias}\``,
    `INNER JOIN \`${tmpCandidate}\` \`c\` ON \`c\`.\`id\` = \`${alias}\`.\`id\``,
    `WHERE ${options.safetyWhere.join('\n  AND ')};`,
    '',
    `INSERT IGNORE INTO \`${backupTable}\``,
    `SELECT \`${alias}\`.*`,
    `FROM \`${options.fromTable}\` \`${alias}\``,
    `INNER JOIN \`${tmpSafe}\` \`s\` ON \`s\`.\`id\` = \`${alias}\`.\`id\`;`,
    '',
    `SELECT COUNT(*) INTO @candidate_${table} FROM \`${tmpCandidate}\`;`,
    `SELECT COUNT(*) INTO @safe_${table} FROM \`${tmpSafe}\`;`,
    `SELECT COUNT(*) INTO @backup_${table} FROM \`${backupTable}\` \`b\` INNER JOIN \`${tmpSafe}\` \`s\` ON \`s\`.\`id\` = \`b\`.\`id\`;`,
    options.deleteSql,
    `SET @deleted_${table} = ROW_COUNT();`,
    '',
    'INSERT INTO `rollback_20260509_generated_summary`',
    '  (`table_name`, `candidate_ids`, `safe_rows`, `backup_rows`, `deleted_rows`)',
    `VALUES (${sqlString(table)}, @candidate_${table}, @safe_${table}, @backup_${table}, @deleted_${table})`,
    'ON DUPLICATE KEY UPDATE',
    '  `candidate_ids` = VALUES(`candidate_ids`),',
    '  `safe_rows` = VALUES(`safe_rows`),',
    '  `backup_rows` = VALUES(`backup_rows`),',
    '  `deleted_rows` = VALUES(`deleted_rows`),',
    '  `ran_at` = CURRENT_TIMESTAMP;',
    '',
    `DROP TEMPORARY TABLE IF EXISTS \`${tmpSafe}\`;`,
    `DROP TEMPORARY TABLE IF EXISTS \`${tmpCandidate}\`;`,
    ''
  );
}

function parseArgs(argv) {
  const parsed = {};
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (!arg.startsWith('--')) {
      continue;
    }
    const key = arg.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith('--')) {
      parsed[key] = true;
    } else {
      parsed[key] = next;
      i += 1;
    }
  }
  return parsed;
}

function groupBy(values, keyFn) {
  const grouped = new Map();
  for (const value of values) {
    const key = keyFn(value);
    if (!grouped.has(key)) {
      grouped.set(key, []);
    }
    grouped.get(key).push(value);
  }
  return grouped;
}

function chunks(values, size) {
  const result = [];
  for (let i = 0; i < values.length; i += size) {
    result.push(values.slice(i, i + size));
  }
  return result;
}

function sqlString(value) {
  return `'${String(value).replace(/'/g, "''")}'`;
}
